  infoln "Generating the admin msp"
  set -x
  fabric-ca-client enroll -u https://ordererAdmin:ordererAdminpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/amu.local/users/Admin@amu.local/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/amu.local/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/amu.local/users/Admin@amu.local/msp/config.yaml"
}
