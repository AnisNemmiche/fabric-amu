    { set +x; } 2>/dev/null

    # Copy the tls CA cert, server cert, server keystore to well known file names in the orderer's tls directory that are referenced by orderer startup config
    cp "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/ca.crt"
    cp "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/signcerts/"* "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/server.crt"
    cp "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/keystore/"* "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/server.key"

    # Copy orderer org's CA cert to orderer's /msp/tlscacerts directory (for use in the orderer MSP definition)
    mkdir -p "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp/tlscacerts"
    cp "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp/tlscacerts/tlsca.amu.local-cert.pem"
