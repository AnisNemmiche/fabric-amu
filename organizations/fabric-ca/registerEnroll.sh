    infoln "Generating the ${ORDERER} MSP"
    set -x
    fabric-ca-client enroll -u https://${ORDERER}:${ORDERER}pw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
    { set +x; } 2>/dev/null

    cp "${PWD}/organizations/ordererOrganizations/amu.local/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp/config.yaml"

    # Workaround: Rename the signcert file to ensure consistency with Cryptogen generated artifacts
    mv "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp/signcerts/cert.pem" "${PWD}/organizations/ordererOrganizations/amu.local/orderers/${ORDERER}.amu.local/msp/signcerts/${ORDERER}.amu.local-cert.pem"

    infoln "Generating the ${ORDERER} TLS certificates, use --csr.hosts to specify Subject Alternative Names"
